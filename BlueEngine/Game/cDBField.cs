using System;

namespace BlueEngine
{
	//----------------------------------------------------------------------------------------------------
	/// <summary>
	///  데이터베이스 필드 객체
	/// </summary>
	//----------------------------------------------------------------------------------------------------
	public class cDBField
	{
		//----------------------------------------------------------------------------------------------------
		#region 상수
		#endregion

		//----------------------------------------------------------------------------------------------------
		#region 변수
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 필드명
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		private			string		m_name;
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 데이터
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		private			object		m_value;
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 데이터 변경 플래그
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		private			bool		m_modify;
		#endregion

		//----------------------------------------------------------------------------------------------------
		#region 속성
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 필드명
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public				string		Name			{get{return m_name;}set{m_name=value;}}
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 데이터
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public				object		Value			{get{return m_value;}set{m_value=value;Modify=true;}}
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 데이터 변경 플래그
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public				bool		Modify			{get{return m_modify;}set{m_modify=value;}}
		#endregion


		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 생성자
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public cDBField( string name, object value )
		{
			Name		= name;
			Value		= value;
			Modify		= false;
		}

		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 데이터베이스 쿼리에 사용될 값 얻기
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public string GetQueryValue()
		{
			if( m_value == typeof(string) )
			{
				return "'" + m_value.ToString() + "'";
			}
			return m_value.ToString();
		}
	}
}
