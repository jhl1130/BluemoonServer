using System;

namespace BlueEngine
{
	//----------------------------------------------------------------------------------------------------
	/// <summary>
	/// 몬스터 객체
	/// </summary>
	//----------------------------------------------------------------------------------------------------
	public class cMonster : cObject
	{
		//----------------------------------------------------------------------------------------------------
		#region 상수
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 최대 몬스터 아이디
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public 		const	ushort		MAX_MONSTER_ID	= 9999;
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 최대 몬스터 정보 아이디
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public 		const	uint		MAX_MONSTER_INFO_ID	= 99999;
		#endregion

		//----------------------------------------------------------------------------------------------------
		#region 변수
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 몬스터 아이디
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		protected			ushort		m_monster_id;
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 몬스터 정보 아이디
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		protected			uint		m_monster_info_id;
		#endregion

		//----------------------------------------------------------------------------------------------------
		#region 속성
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 몬스터 아이디
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public				ushort		MonsterID			{get{return m_monster_id;}}
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 몬스터 아이디
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public				uint		MonsterInfoID		{get{return m_monster_info_id;}}
		#endregion

		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 생성자
		/// </summary>
		/// <param name="monster_id">몬스터 아이디</param>
		/// <param name="monster_info_id">몬스터 정보 아이디</param>
		//----------------------------------------------------------------------------------------------------
		public cMonster( ushort monster_id, uint monster_info_id )
		{
			m_monster_id		= monster_id;
			m_monster_info_id	= monster_info_id;
		}
	}
}
