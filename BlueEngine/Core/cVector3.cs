using System;
using System.Diagnostics;
using System.Reflection;

namespace BlueEngine
{
	//----------------------------------------------------------------------------------------------------
	/// <summary>
	/// 벡터3
	/// </summary>
	//----------------------------------------------------------------------------------------------------
	public class cVector3 : cObject
	{
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 최소 단위
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public const float EPSILON = 1e-005f;

		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// x축 값
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public float	x;
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// y축 값
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public float	y;
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// z축 값
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public float	z;

		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 모든 축이 0으로 초기화된 값
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public cVector3	Zero	{get{return new cVector3();}}

		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 생성자
		/// </summary>
		//----------------------------------------------------------------------------------------------------
		public cVector3():this(0,0,0)
		{
		}
		//----------------------------------------------------------------------------------------------------
		/// <summary>
		/// 생성자
		/// </summary>
		/// <param name="x">x축값</param>
		/// <param name="y">y축값</param>
		/// <param name="z">z축값</param>
		//----------------------------------------------------------------------------------------------------
		public cVector3(float x, float y, float z)
		{
			this.x = x;
			this.y = y;
			this.z = z;
		}
	}
}

